public class KotaArray {
    public static void main(String[] args) {
        String kotaIndonesia[] = {"Jakarta", "Surabaya", "Bandung", "Medan", "Semarang", "Yogyakarta", "Makassar"};
        
        System.out.println("Daftar Kota di Indonesia:");
        for (int i = 0; i < kotaIndonesia.length; i++){
            System.out.println(kotaIndonesia[i]);
        }
    }
    
}
